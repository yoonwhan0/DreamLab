/**
 * Branch/.env → .env_chat → .env 순으로 API 키를 읽어 .env.local 생성.
 * bat·netlify dev·vite가 OPENAI_API_KEY 등을 사용할 수 있게 함.
 */
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

const ROOT = path.resolve(import.meta.dirname, "..");
const OUT = path.join(ROOT, ".env.local");

const BRANCH_DIR = path.join(ROOT, "Branch");
const BRANCH_ENV = path.join(BRANCH_DIR, ".env");
const CHAT_ENV = path.join(ROOT, ".env_chat");

const SOURCES = [
  { file: BRANCH_ENV, label: "Branch/.env" },
  { file: CHAT_ENV, label: ".env_chat" },
  { file: path.join(ROOT, ".env"), label: ".env" },
];

function ensureBranchEnvFromChat() {
  if (existsSync(BRANCH_ENV) || !existsSync(CHAT_ENV)) return;

  const chatVars = loadSource(CHAT_ENV);
  const openai = chatVars.get("OPENAI_API_KEY");
  const gemini = chatVars.get("GEMINI_API_KEY");
  if (!openai && !gemini) return;

  const lines = ["# Migrated from .env_chat — Branch keys take priority in bat"];
  if (openai) lines.push(`OPENAI_API_KEY=${openai}`);
  if (gemini) lines.push(`GEMINI_API_KEY=${gemini}`);
  writeFileSync(BRANCH_ENV, `${lines.join("\n")}\n`, "utf8");
  console.log("→ Branch/.env 생성 (.env_chat에서 이전)");
}

const API_KEYS = new Set(["OPENAI_API_KEY", "GEMINI_API_KEY"]);

function parseEnvFile(content) {
  const vars = new Map();

  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;

    const eq = line.indexOf("=");
    if (eq > 0) {
      const key = line.slice(0, eq).trim();
      const value = line.slice(eq + 1).trim().replace(/^["']|["']$/g, "");
      if (key) vars.set(key, value);
      continue;
    }

    // raw API key line (sk-... / AIza...)
    if (/^sk-[A-Za-z0-9_-]+$/.test(line)) {
      vars.set("OPENAI_API_KEY", line);
    } else if (line.startsWith("AIza")) {
      vars.set("GEMINI_API_KEY", line);
    }
  }

  return vars;
}

function loadSource(file) {
  if (!existsSync(file)) return new Map();
  return parseEnvFile(readFileSync(file, "utf8"));
}

ensureBranchEnvFromChat();

function mergeMaps(...maps) {
  const out = new Map();
  for (const map of maps) {
    for (const [k, v] of map) {
      if (v) out.set(k, v);
    }
  }
  return out;
}

const branch = loadSource(SOURCES[0].file);
const chat = loadSource(SOURCES[1].file);
const env = loadSource(SOURCES[2].file);

// Branch > .env_chat > .env (API keys only for override chain)
const apiFromBranch = new Map([...branch].filter(([k]) => API_KEYS.has(k)));
const apiFromChat = new Map([...chat].filter(([k]) => API_KEYS.has(k)));
const apiFromEnv = new Map([...env].filter(([k]) => API_KEYS.has(k)));

const apiKeys = mergeMaps(apiFromEnv, apiFromChat, apiFromBranch);

// VITE_* and other client vars from .env (not overridden by Branch unless set there)
const clientVars = new Map([...env, ...branch].filter(([k]) => k.startsWith("VITE_")));

const lines = [
  "# Auto-generated by scripts/sync-branch-env.mjs — do not commit",
  `# ${new Date().toISOString()}`,
  "",
];

for (const [key, value] of clientVars) {
  lines.push(`${key}=${value}`);
}

if (clientVars.size > 0 && apiKeys.size > 0) lines.push("");

for (const [key, value] of apiKeys) {
  lines.push(`${key}=${value}`);
}

writeFileSync(OUT, `${lines.join("\n")}\n`, "utf8");

const used = [];
if (apiFromBranch.size > 0) used.push("Branch/.env");
else if (apiFromChat.size > 0) used.push(".env_chat");
else if (apiFromEnv.size > 0) used.push(".env");

const openai = apiKeys.has("OPENAI_API_KEY");
const gemini = apiKeys.has("GEMINI_API_KEY");

if (openai || gemini) {
  console.log(
    `✓ API keys loaded → .env.local (${used.join(" → ") || "sources"})` +
      ` [OpenAI: ${openai ? "yes" : "no"}, Gemini: ${gemini ? "yes" : "no"}]`,
  );
} else {
  console.warn(
    "⚠ API key 없음 — Branch/.env 또는 .env_chat에 OPENAI_API_KEY를 넣으세요. (mock 해석 사용)",
  );
}

const hasFirebase = clientVars.has("VITE_FIREBASE_PROJECT_ID") && clientVars.has("VITE_FIREBASE_API_KEY");
if (!hasFirebase) {
  console.warn(
    "⚠ VITE_FIREBASE_* 없음 — .env에 Firebase 웹 설정을 넣으세요. (익명 로그인·DB 저장 불가, UI만 동작)",
  );
}
